public class App {
    public static void main(String[] args) throws Exception {
        //  Estrutura de Repetição WHILE -> Sintaxe
        
        //  Criação e inicialização de uma variável que servirá de contador e valor inicial.
        int contador = 0;
        /*
         *  Dentro dos parênteses ficará a condição para execução do loop(repetição),
         *  primeiro ele checa a condição e se o resultado da checagem for o esperado 
         *  ele executa o loop. 
         */
        while(contador <= 10){

            /*
             *  Bloco de código.
             */

            //  Depois da execução do bloco de código, será incremementado +1 ao valor da variável. 
            contador++;
        }
    }
}
