public class App {
    public static void main(String[] args) throws Exception {
        // Calcule e mostre a média de três números inteiros.
        int primeiroValor = 50;
        int segundoValor = 30;
        int terceiroValor = 80;
        int mediaInteiros = (primeiroValor+segundoValor+terceiroValor) / 2;
        
        System.out.println("A Média é: " + mediaInteiros);
    }
}