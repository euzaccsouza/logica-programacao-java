public class App {
    public static void main(String[] args) throws Exception {
        // Altere o valor de uma variável três vezes e mostre as mudanças.
        double valor = 5700.00;
        System.out.println("Valor Inicial: " + valor);
        valor  = 800.00;
        System.out.println("Valor atual: " + valor);
        valor = 600.00;
        System.out.println("Valor atual: " + valor);
        valor = 150.00;
        System.out.println("Valor atual: "+ valor );

    }
}
