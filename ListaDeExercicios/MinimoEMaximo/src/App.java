public class App {
    public static void main(String[] args) throws Exception {
        // Utilize métodos da classe Integer para obter o valor mínimo e máximo.
        int primeiroValor = 1996;
        int segundoValor = 2025;

        System.out.println(Integer.min(primeiroValor, segundoValor)); // O menor valor será de: 1996
        System.out.println(Integer.max(primeiroValor, segundoValor)); // O maior valor será de: 2025


    }
}
